const deckLines = [];
let totalCards = 0;
let pokemonCount = pokemon.reduce((s, e) => s + e.qty, 0);
let trainerCount = trainers.reduce((s, e) => s + e.qty, 0);
let energyCount = energy.reduce((s, e) => s + e.qty, 0);

deckLines.push("****** Pok\u00e9mon Trading Card Game Deck List ******");
